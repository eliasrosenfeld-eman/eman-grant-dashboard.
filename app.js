let activeVerdict = "All";
let activeDeadline = "All";
let searchTerm = "";
let selectedName = grants[0].name;

const metrics = [
  ["Tracked", grants.length],
  ["Pursue", grants.filter((g) => g.verdict === "Pursue").length],
  ["Within 30 days", grants.filter((g) => g.dueFlag.includes("30")).length],
  ["Material pipeline", grants.filter((g) => g.materiality.toLowerCase().includes("material")).length],
];

function normalize(value) {
  return String(value).toLowerCase();
}

function matchesSearch(grant) {
  if (!searchTerm) return true;
  return [
    grant.name,
    grant.tier,
    grant.size,
    grant.deadline,
    grant.eligible,
    grant.why,
    grant.requirements,
    grant.verdict,
    grant.status,
    grant.nextStep,
  ]
    .map(normalize)
    .join(" ")
    .includes(normalize(searchTerm));
}

function filteredGrants() {
  return grants.filter((grant) => {
    const verdictMatch = activeVerdict === "All" || grant.verdict === activeVerdict;
    const deadlineMatch = activeDeadline === "All" || grant.dueFlag === activeDeadline;
    return verdictMatch && deadlineMatch && matchesSearch(grant);
  });
}

function verdictClass(verdict) {
  return verdict.toLowerCase();
}

function renderMetrics() {
  document.getElementById("metrics").innerHTML = metrics
    .map(
      ([label, value]) => `
        <div class="metric">
          <span>${label}</span>
          <strong>${value}</strong>
        </div>
      `,
    )
    .join("");
}

function renderFilters() {
  const verdicts = ["All", "Pursue", "Maybe", "Skip"];
  const deadlines = ["All", ...Array.from(new Set(grants.map((g) => g.dueFlag)))];
  document.getElementById("verdictFilters").innerHTML = verdicts
    .map(
      (verdict) =>
        `<button class="chip ${activeVerdict === verdict ? "active" : ""}" data-filter="verdict" data-value="${verdict}">${verdict}</button>`,
    )
    .join("");
  document.getElementById("deadlineFilters").innerHTML = deadlines
    .map(
      (deadline) =>
        `<button class="chip ${activeDeadline === deadline ? "active" : ""}" data-filter="deadline" data-value="${deadline}">${deadline}</button>`,
    )
    .join("");
}

function renderRows() {
  const rows = filteredGrants();
  const tbody = document.getElementById("grantRows");
  document.getElementById("resultCount").textContent = `${rows.length} of ${grants.length} shown`;

  if (!rows.length) {
    tbody.innerHTML = `<tr><td class="empty" colspan="6">No grants match the current filters.</td></tr>`;
    renderDetail(null);
    return;
  }

  if (!rows.some((grant) => grant.name === selectedName)) {
    selectedName = rows[0].name;
  }

  tbody.innerHTML = rows
    .map(
      (grant) => `
        <tr class="${grant.name === selectedName ? "selected" : ""}" data-name="${grant.name}">
          <td>
            <span class="grant-name">${grant.name}</span>
            <span class="tier">${grant.tier}</span>
          </td>
          <td>${grant.size}</td>
          <td>${grant.deadline}<span class="deadline-flag">${grant.dueFlag}</span></td>
          <td>${grant.why}</td>
          <td>${grant.requirements}</td>
          <td><span class="verdict ${verdictClass(grant.verdict)}">${grant.verdict}</span></td>
        </tr>
      `,
    )
    .join("");

  renderDetail(grants.find((grant) => grant.name === selectedName));
}

function renderDetail(grant) {
  const detail = document.getElementById("detail");
  if (!grant) {
    detail.innerHTML = `<p class="empty">Select a row to inspect the opportunity.</p>`;
    return;
  }

  detail.innerHTML = `
    <h2>${grant.name}</h2>
    <span class="verdict ${verdictClass(grant.verdict)}">${grant.verdict}</span>
    <div class="detail-grid">
      <div class="detail-item"><span>Eligible applicant</span><p>${grant.eligible}</p></div>
      <div class="detail-item"><span>Materiality</span><p>${grant.materiality}</p></div>
      <div class="detail-item"><span>Effort / odds</span><p>${grant.effort}; ${grant.odds}</p></div>
      <div class="detail-item"><span>Status</span><p>${grant.status}</p></div>
      <div class="detail-item"><span>Next step</span><p>${grant.nextStep}</p></div>
    </div>
    <a class="source-link" href="${grant.source}" target="_blank" rel="noreferrer">Open source</a>
  `;
}

function renderLandscape() {
  document.getElementById("landscape").innerHTML = landscapeRows
    .map(
      (row) => `
        <article class="landscape-row">
          <h3>${row.segment}</h3>
          <p><strong>${row.materiality}</strong></p>
          <p>${row.evidence}</p>
          <p>${row.implication}</p>
        </article>
      `,
    )
    .join("");
}

function renderSources() {
  document.getElementById("sources").innerHTML = sources
    .map(
      ([name, url]) => `
        <article class="source-item">
          <h3>${name}</h3>
          <p>Primary source used in the dashboard.</p>
          <a href="${url}" target="_blank" rel="noreferrer">${url}</a>
        </article>
      `,
    )
    .join("");
}

function render() {
  renderMetrics();
  renderFilters();
  renderRows();
  renderLandscape();
  renderSources();
}

document.addEventListener("click", (event) => {
  const chip = event.target.closest(".chip");
  if (chip) {
    if (chip.dataset.filter === "verdict") activeVerdict = chip.dataset.value;
    if (chip.dataset.filter === "deadline") activeDeadline = chip.dataset.value;
    render();
    return;
  }

  const row = event.target.closest("tbody tr[data-name]");
  if (row) {
    selectedName = row.dataset.name;
    renderRows();
  }
});

document.getElementById("search").addEventListener("input", (event) => {
  searchTerm = event.target.value;
  renderRows();
});

render();
